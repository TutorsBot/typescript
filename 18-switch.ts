var key = "FAZLUR RAHMAN"
switch (key) {
    case "Rahman":
       console.log(key);
       console.log("Key Matched");
    break;
    case "Fazlur":
        console.log(key);
        console.log("Key Matched");
    break;
    default:
        console.log("No Name Matched for "+key);
    break;
}

